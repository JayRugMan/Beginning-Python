# Line_Characters.py
class Tables:
    '''Characters for table'''

    def __init__(self):
        self.tblCross = '┼'
        self.tblTop = '─'
        self.tblEdges = '│'


class Boxes:
    '''Characters for boxes'''

    def __init__(self):
        self.tpBtm = '━'
        self.cornerTL = '┏'
        self.cornerTR = '┓'
        self.cornerBR = '┛'
        self.cornerBL = '┗'
        self.edges = '┃'
